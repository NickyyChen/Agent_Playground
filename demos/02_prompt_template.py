import sys, os
sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))
from shared.llm_client import chat


# ══════════════════════════════════════════════════════════════
# 演示1：System Prompt 质量对比 —— 模糊人设 vs 精细人设
# ══════════════════════════════════════════════════════════════

# WHY: 模糊人设给模型的约束太少，模型会自由发挥 → 可能编造政策、遗漏关键步骤
VAGUE_SYSTEM = "你是一个客服助手，帮助用户解决问题。"

# WHY: 精细人设明确了身份、服务范围、禁止行为 → 回答可预期、不越界
#      关键要素：你是谁 / 你能做什么 / 你不能做什么 / 你的回答风格
PRECISE_SYSTEM = """你是"小选"，好买电商平台的售后专员。
职责：
- 处理退换货、退款、投诉等售后问题
- 严格依据平台政策回答，不可编造规则
- 需要查询订单状态时，引导用户提供订单号
平台政策速查：
- 退货：签收7天内，商品完好、配件齐全可退，运费平台承担
- 换货：签收15天内，质量问题免费换新，人为损坏不换
- 退款：退货签收后3个工作日内原路退回
禁止：
- 禁止承诺赔偿金额
- 禁止透露内部流程
- 不确定时引导用户查看帮助中心
回答风格：专业简洁，每条引用具体政策条款"""


def demo_system_prompt_quality():
    """
    演示：同一个问题，模糊人设 vs 精细人设的回答差异。
    WHY: 模糊人设让模型"自己脑补"政策 → 可能编造虚假规则，在客服场景是灾难；
         精细人设把政策写在 prompt 里 → 模型只需要引用，准确度大幅提升。
         这证明：prompt 不是"随便写一句"，而是要把领域知识编码进去。
    """
    print("=" * 60)
    print(" 演示1：模糊人设 vs 精细人设")
    print("=" * 60)

    question = "我的耳机用了5天左耳没声音了，能换新吗？"

    for label, system in [("模糊人设", VAGUE_SYSTEM), ("精细人设", PRECISE_SYSTEM)]:
        reply = chat(
            [{"role": "system", "content": system},
             {"role": "user", "content": question}],
            temperature=0.1,  # WHY: 用低温确保对比的是 prompt 差异，不是随机性
        )
        print(f"【{label}】")
        print(f"  回答: {reply}")
        print()


# ══════════════════════════════════════════════════════════════
# 演示2：Prompt 模板 + 变量注入
# ══════════════════════════════════════════════════════════════

# WHY: 真实客服系统不是对着一句固定 prompt 发问 —— 用户姓名、订单号、
#      商品名、物流状态都是运行时才知道的。用 Python f-string 把变量
#      注入到 prompt 模板中，每次请求都生成"个性化 prompt"。
TEMPLATE_SYSTEM = """你是"小选"，好买电商的智能客服。
当前对话的用户信息（由系统自动注入）：
- 用户昵称：{user_name}
- 会员等级：{member_level}
- 最近订单：{recent_order}
回答时请自然地使用用户昵称，根据会员等级调整语气：
- 普通会员：标准客气
- 金牌会员：更加热情，可使用"您"敬语
- 钻石会员：尊享口吻，优先处理"""


def demo_template_injection():
    """
    演示：用模板将运行时数据注入 prompt。
    WHY: 为什么不用字符串拼接？因为模板分离了"结构"和"数据"——
         TEMPLATE_SYSTEM 定义客服人设和变量占位，
         format() 调用时填入具体用户数据，
         这样改人设不改数据逻辑，改数据不改人设逻辑。
    """
    print("=" * 60)
    print(" 演示2：Prompt 模板 + 变量注入")
    print("=" * 60)

    # WHY: 模拟三个不同会员等级的用户，同一个问题，回答语气应不同
    users = [
        {"user_name": "小明", "member_level": "普通会员",
         "recent_order": "ORD001 - 蓝牙耳机（待发货）"},
        {"user_name": "李总", "member_level": "钻石会员",
         "recent_order": "ORD002 - 旗舰降噪耳机（已签收）"},
    ]

    question = "我的订单什么时候发货？"

    for user in users:
        # WHY: format(**user) 把字典解包成关键字参数，
        #      模板中的 {user_name} 自动替换为 user["user_name"]
        filled_system = TEMPLATE_SYSTEM.format(**user)
        reply = chat(
            [{"role": "system", "content": filled_system},
             {"role": "user", "content": question}],
            temperature=0.1,
        )
        print(f" 用户: {user['user_name']}（{user['member_level']}）")
        print(f" 客服: {reply}")
        print()


# ══════════════════════════════════════════════════════════════
# 演示3：Few-shot 示例 —— 零样本 vs 少样本
# ══════════════════════════════════════════════════════════════

# WHY: 当输出格式有严格要求时（如客服需要产出固定 JSON 工单摘要），
#      光用文字描述"请输出JSON"不够 —— 模型可能多输出解释文字，
#      或者字段名跟你要求的不一致。
#      Few-shot 的核心思路：在 prompt 里塞 2~3 个 (输入→输出) 范例，
#      模型会自动学会"照这个格式来"，比纯文字描述稳定得多。

ZERO_SHOT_SYSTEM = """你是客服工单摘要助手。根据对话内容，输出一个 JSON 格式的工单摘要。
JSON 必须包含字段：category（问题分类）、urgency（紧急程度：low/medium/high）、
summary（一句话摘要）、action（建议处理方式）"""

FEW_SHOT_SYSTEM = """你是客服工单摘要助手。根据对话内容，输出一个 JSON 格式的工单摘要。

【输出格式示例】
用户说"物流显示签收但我没收到"
→ {"category": "物流纠纷", "urgency": "high", "summary": "用户反馈物流显示已签收但实际未收到商品", "action": "联系物流公司核实签收记录，同步补发或退款"}

用户说"这个优惠券怎么用不了"
→ {"category": "优惠券咨询", "urgency": "low", "summary": "用户咨询优惠券使用规则", "action": "核实优惠券有效期和适用品类，引导用户重新下单"}

用户说"耳机有杂音，我要退货"
→ {"category": "商品质量投诉", "urgency": "medium", "summary": "用户反馈耳机存在杂音质量问题要求退货", "action": "引导用户拍摄故障视频，确认后走7天无理由退货流程"}

请严格按照以上格式，只输出 JSON，不要输出任何解释文字。"""


def demo_few_shot():
    """
    演示：零样本 vs 少样本 prompt 对输出格式的控制力。
    WHY: 零样本（zero-shot）只给指令不给示例 → 模型可能输出多余解释
         或 JSON 格式不符合预期；
         少样本（few-shot）给 2~3 个 (输入→输出) 范例 → 模型自动
         学会输出格式，稳定性和一致性显著提升。
         在生产客服系统中，工单摘要必须严格结构化才能入库，
         few-shot 是保证格式一致性的低成本方案。
    """
    print("=" * 60)
    print(" 演示3：零样本 vs 少样本（Few-shot）")
    print("=" * 60)

    dialogue = """用户：我前天买的手机壳，今天收到发现发错颜色了，我要换货。
客服：好的，非常抱歉给您带来不便。请问订单号是多少？
用户：ORD20240056
客服：已查到您的订单，是黑色款发成了蓝色对吗？
用户：对，我要黑色的。
客服：明白，这个情况符合换货条件。我这就帮您申请换货。"""

    for label, system in [("零样本（纯指令）", ZERO_SHOT_SYSTEM),
                           ("少样本（3个示例）", FEW_SHOT_SYSTEM)]:
        reply = chat(
            [{"role": "system", "content": system},
             {"role": "user",
              "content": f"请为以下客服对话生成工单摘要：\n\n{dialogue}"}],
            temperature=0.1,
        )
        print(f"【{label}】")
        print(f"  输出: {reply}")
        print()


def main():
    print()
    print("╔══════════════════════════════════════════════════╗")
    print("║  Agent-Playground Demo 02: Prompt Engineering     ║")
    print("║  人设质量 · 模板注入 · Few-shot                   ║")
    print("╚══════════════════════════════════════════════════╝")
    print()

    demo_system_prompt_quality()
    demo_template_injection()
    demo_few_shot()

    print("=" * 60)
    print(" Demo 02 完成！")
    print("=" * 60)


if __name__ == "__main__":
    main()
